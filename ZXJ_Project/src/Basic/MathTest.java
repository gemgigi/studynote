package Basic;

public class MathTest {

	public static void main(String[] args) {

		System.out.println(Math.round(-8.5)); //-8
		System.out.println(Math.round(8.5));  //9
		
		System.out.println(Math.ceil(-8.5)); //-8
		System.out.println(Math.ceil(8.5));  //9
		
		System.out.println(Math.floor(-8.5)); //-9
		System.out.println(Math.floor(8.5)); //8
		
	}
}
